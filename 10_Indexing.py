# Acessesing elements of a sequence using the indexing operator[]
# indexing[]= [start:end:step]

# Note: During indexing , after this ":" symbol , index -1
#                         before this ":" symbol, index as it is
num='14818882882897'
print(num[3])      # prints the character with index 4 (starting from index= 0)
print(num[:4])     # starts from the index 0 to index 3 ( index 4 is excluded)
print(num[4:])     # Starts from the index 3 till the end.

print(num[3:5])    # starts from 3rd character to 5th character 
